"""Unified run-report generator.

Scans the artifacts written by the classical, PyTorch, and kernel-benchmark
pipelines and produces a single human-readable markdown report under
``outputs/reports/run_summary.md``. The report is intentionally self-contained
so a reviewer can read it without having to open every JSON.
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path

from utils.io_utils import ensure_directories

LOGGER = logging.getLogger(__name__)


@dataclass
class ReportPaths:
    metrics_dir: Path
    reports_dir: Path
    output_path: Path


def generate_run_report(output_dir: Path) -> Path:
    """Walk the metrics directory and write a unified markdown summary.

    Returns the path to the markdown file. Sections that have no data are
    skipped silently so the report stays meaningful when only some pipelines
    have been run.
    """
    paths = _resolve_report_paths(output_dir)
    ensure_directories([paths.reports_dir])

    sections: list[str] = []
    sections.append(_render_header())

    classical_section = _render_classical_section(paths.metrics_dir)
    if classical_section:
        sections.append(classical_section)

    pytorch_section = _render_pytorch_section(paths.metrics_dir)
    if pytorch_section:
        sections.append(pytorch_section)

    benchmark_section = _render_benchmark_section(paths.metrics_dir)
    if benchmark_section:
        sections.append(benchmark_section)

    if len(sections) == 1:
        sections.append(
            "_No metric files were found under `outputs/metrics/`. Run a pipeline "
            "first, for example `python3 main.py classical-baselines`._"
        )

    paths.output_path.write_text("\n\n".join(sections) + "\n")
    LOGGER.info("Wrote unified run report to %s", paths.output_path)
    return paths.output_path


def _resolve_report_paths(output_dir: Path) -> ReportPaths:
    return ReportPaths(
        metrics_dir=output_dir / "metrics",
        reports_dir=output_dir / "reports",
        output_path=output_dir / "reports" / "run_summary.md",
    )


def _render_header() -> str:
    timestamp = datetime.now(timezone.utc).isoformat(timespec="seconds")
    return (
        "# GPU Image Classifier — Run Summary\n\n"
        f"_Generated at {timestamp}._\n\n"
        "This document combines the latest classical baseline, PyTorch training, "
        "and kernel benchmark results into a single view. It is regenerated by "
        "`python3 main.py generate-report` and reads from `outputs/metrics/`."
    )


def _render_classical_section(metrics_dir: Path) -> str | None:
    summary_path = metrics_dir / "classical_results.json"
    if not summary_path.exists():
        return None

    payload = json.loads(summary_path.read_text())
    rows = payload.get("results", [])
    if not rows:
        return None

    dataset = payload.get("dataset", "unknown")
    best = payload.get("best_model_by_validation_accuracy", "—")

    lines = [
        "## Classical Baselines",
        "",
        f"- Dataset: `{dataset}`",
        f"- Best model by validation accuracy: **{best}**",
        f"- Split sizes: {payload.get('split_sizes', {})}",
        "",
        "| Model | Train time (s) | Val acc | Val F1 | Test acc | Test F1 | Test ROC-AUC |",
        "|---|---:|---:|---:|---:|---:|---:|",
    ]
    for row in rows:
        validation = row["validation"]["metrics"]
        test = row["test"]["metrics"]
        lines.append(
            "| {model} | {train:.2f} | {va:.3f} | {vf:.3f} | {ta:.3f} | {tf:.3f} | {ra} |".format(
                model=row["model_name"],
                train=row["train_time_seconds"],
                va=validation["accuracy"],
                vf=validation["f1_weighted"],
                ta=test["accuracy"],
                tf=test["f1_weighted"],
                ra=_format_optional(test.get("roc_auc_ovr_weighted")),
            )
        )
    return "\n".join(lines)


def _render_pytorch_section(metrics_dir: Path) -> str | None:
    pytorch_files = sorted(metrics_dir.glob("*_results.json"))
    pytorch_payloads: list[dict[str, object]] = []
    for path in pytorch_files:
        if path.name == "classical_results.json":
            continue
        try:
            payload = json.loads(path.read_text())
        except json.JSONDecodeError:
            continue
        if "model_name" not in payload or "history" not in payload:
            continue
        pytorch_payloads.append(payload)

    if not pytorch_payloads:
        return None

    lines = [
        "## PyTorch Training Runs",
        "",
        "| Dataset | Model | Device | Best epoch | Trainable params | Val acc | Test acc | Test F1 | Test ROC-AUC |",
        "|---|---|---|---:|---:|---:|---:|---:|---:|",
    ]
    for payload in pytorch_payloads:
        test_metrics = payload.get("test", {}).get("metrics", {})
        validation_metrics = (
            payload.get("final_validation", {}).get("metrics", {})
            or payload.get("best_validation_metrics", {})
            or {}
        )
        lines.append(
            "| {dataset} | {model} | {device} | {best} | {params:,} | {va} | {ta} | {tf} | {ra} |".format(
                dataset=payload.get("dataset", "—"),
                model=payload.get("model_name", "—"),
                device=payload.get("device", "—"),
                best=payload.get("best_epoch", "—"),
                params=payload.get("trainable_parameters", 0),
                va=_format_metric(validation_metrics.get("accuracy")),
                ta=_format_metric(test_metrics.get("accuracy")),
                tf=_format_metric(test_metrics.get("f1_weighted")),
                ra=_format_optional(test_metrics.get("roc_auc_ovr_weighted")),
            )
        )
    return "\n".join(lines)


def _render_benchmark_section(metrics_dir: Path) -> str | None:
    benchmark_paths = sorted(metrics_dir.glob("image_normalization_*_benchmark.json"))
    if not benchmark_paths:
        return None

    section_lines = ["## Kernel Benchmarks (image normalization)"]
    for path in benchmark_paths:
        try:
            payload = json.loads(path.read_text())
        except json.JSONDecodeError:
            continue
        shape = payload.get("tensor_shape", [])
        shape_label = "x".join(str(value) for value in shape) if shape else path.stem

        section_lines.append("")
        section_lines.append(f"### Tensor shape `{shape_label}`")
        section_lines.append("")
        section_lines.append(
            f"- Warmup iters: {payload.get('warmup_iterations', '—')}"
            f" | measured iters: {payload.get('benchmark_iterations', '—')}"
            f" | torch: {payload.get('environment', {}).get('torch_version', '—')}"
            f" | CUDA available: {payload.get('environment', {}).get('cuda_available', '—')}"
        )
        section_lines.append("")
        section_lines.append(
            "| Backend | Status | Median ms | Std ms | Bandwidth (GB/s) | Speedup vs CPU |"
        )
        section_lines.append("|---|---|---:|---:|---:|---:|")
        for row in payload.get("results", []):
            section_lines.append(
                "| {backend} | {status} | {med} | {std} | {bw} | {sp} |".format(
                    backend=row.get("backend", "—"),
                    status=row.get("status", "—"),
                    med=_format_metric(row.get("median_latency_ms")),
                    std=_format_metric(row.get("std_latency_ms")),
                    bw=_format_metric(row.get("effective_bandwidth_gbps")),
                    sp=_format_optional(row.get("speedup_vs_cpu")),
                )
            )
    return "\n".join(section_lines)


def _format_metric(value) -> str:
    if value is None:
        return "—"
    try:
        return f"{float(value):.3f}"
    except (TypeError, ValueError):
        return str(value)


def _format_optional(value) -> str:
    if value is None:
        return "—"
    try:
        return f"{float(value):.3f}"
    except (TypeError, ValueError):
        return str(value)
